    public static int fibonacci(int n) {

        if (n == 0) {
            return 0;
        } else if (n == 1) {
            return 1;
        } else {
            return fibonacci(n - 1) + fibonacci(n - 2);
        }

    }
    public static String serie(int n) {
        String series = "";
        for (int i = 0; i <= n; i++) {
            series += fibonacci(i);
            if (i < n) {
                series += ", ";
            }
        }
        return series;
    }

